// program to compare between to number

class IfDem
{ 	
	public static void main(String args[])
	{
		int a=25,b=55;
		
		if(a>b) //this returns to boolean expression True or False
		{	
			System.out.println(a+"is greater than"+b);
		}
		if(a<b)
		{
			System.out.println(b+"is less than"+a);
		}
	} 
}